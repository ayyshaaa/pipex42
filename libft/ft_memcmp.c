#include "libft.h"
#include <string.h>

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned int	i;
	unsigned char	*str1;
	unsigned char	*str2;

	str1 = (unsigned char *)s1;
	str2 = (unsigned char *)s2;
	i = 0;
	if (n == 0 || (str1 == NULL && str2 == NULL))
		return (0);
	while (str1[i] == str2[i] && i < (n - 1))
		i++;
	return (str1[i] - str2[i]);
}
